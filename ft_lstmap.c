#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_lst;
	t_list	*new_node;
	t_list	*ptr;

	if (!lst || !f)
		return (NULL);
	new_lst = NULL;
	ptr = lst;
	while (ptr)
	{
		new_node = ft_lstnew(f(ptr->content));
		if (!new_node)
		{
			ft_lstclear(&new_lst, del);
			return (NULL);
		}
		ft_lstadd_back(&new_lst, new_node);
		ptr = ptr->next;
	}
	return (new_lst);
}
